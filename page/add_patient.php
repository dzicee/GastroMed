        $(document).ready(function() {

            $("#submit").click(function() {

                $.post(
                    'index.php?add', // Un script PHP que l'on va créer juste après
                    {
                        nom: $("#nom").val(), // Nous récupérons la valeur de nos inputs que l'on fait passer à connexion.php
                        prenom: $("#prenom").val()
                    },

                    function(data) {

                        if (data == 'Success1') {

                            $("#resultat").html("<p>Patient crée avec succès !</p>");
                        } else if (data == 'exist1') {


                            $("#resultat").html("<p>Erreur lors de la création...</p>");
                        } else if (data == 'vide1') {


                            $("#resultat").html("<p>Erreur, veuillez renseigner les cahmps Nom et Prenom</p>");
                        }


                    },

                    'text' // Nous souhaitons recevoir "Success" ou "Failed", donc on indique text !
                );

            });

        });
